export default function useTeams() {
  return {
    allTeams: DATA,
    jugende: getJugende(),
    jugendeList: getList(),
    aktiveList: getList('aktive'),
    aktive: getAktive()
  }
}

function getJugende() {
  let jugende = {}
  for (const key in DATA) {
    if (!['1M', '2M', 'Alte-herren'].includes(key)) {
      jugende[key] = DATA[key]
    }
  }
  return jugende
}

function getAktive() {
  let aktive = {}
  for (const key in DATA) {
    if (['1M', '2M'].includes(key)) {
      aktive[key] = DATA[key]
    }
  }
  return aktive
}

function getList(team = 'jugende') {
  let teamType = null
  if (team === 'jugende') {
    teamType = getJugende()
  } else {
    teamType = getAktive()
  }
  const list = []
  for (const key in teamType) {
    list.push({ id: key, name: teamType[key].teamName })
  }
  return list
}
